function GetFileExtension(url)
  return url:match("^.+(%..+)$")
end


function contains(tab, val)
   for index, value in ipairs(tab) do
        if value == val then
            return true
        end
    end
   return false
end

require("telescope").setup {
  defaults = {
    preview = {
      filesize_limit = 50,
      mime_hook = function(filepath, bufnr, opts)
        local fileext = GetFileExtension(filepath):lower()
        local command = nil

        if contains({'.png','.ico','.jpg','.bmp'}, fileext) then
          command =           {
            "viu",
            filepath
          }
        elseif contains({'.pdf'}, fileext) then
          command =           {
            "pdftoppm"," -jpeg -f 1 -singlefile ",
            filepath,
            "| ",
            "viu - "
          }
        else
          command  = {
            "xxd", "-l 256", filepath
          }
        end
        if command then
          local term = vim.api.nvim_open_term(bufnr, {})
          local function send_output(_, data, _ )
            for _, d in ipairs(data) do
              vim.api.nvim_chan_send(term, d..'\r\n')
            end
          end
          vim.fn.jobstart(
          command,
          {on_stdout=send_output, stdout_buffered=true, pty=true}
          )
        else
          require("telescope.previewers.utils").
          set_preview_message(
          bufnr,
          opts.winid,
          "Binary ".. fileext .." cannot be previewed"
          )
        end
      end
    },
  }
}


